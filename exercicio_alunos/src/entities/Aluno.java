package entities;

public class Aluno {
	
	public String name;
	public int n1;
	public int n2;
	public int n3;

	public void verificarNotas() {
		int notas = n1+n2+n3; 
		if( notas < 60 ) {
			System.out.println("Aluno Reprovado, Indice final "+notas);
		}else {
			System.out.println("Aluno Aprovado, Indice final "+notas);
		}
	}
	
	
	public String toString() {
		return "Aluno: "+ name + " N1 => "+n1+" N2 => "+n2+" N3 => "+n3;
	}
}




